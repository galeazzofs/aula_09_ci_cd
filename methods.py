def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter


def subtrai(a,b):
    sub = a - b
    return sub

def multiplicacao(a, b):
    mult = a * b
    return mult

def elevaQuadrado(a):
    quad = a*a
    return quad

def divisao(a,b):
    div = a/b
    return div

def soma(a, b):
    soma = a + b
    return soma